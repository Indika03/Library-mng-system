    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        textID.setEditable(false);
         jButton1.setEnabled(false);
        textID.setText(dft.getValueAt(selectedIndex, 0).toString());
        textName.setText(dft.getValueAt(selectedIndex, 1).toString());

        String as = dft.getValueAt(selectedIndex, 2).toString();
        int AS = Integer.parseInt(as);
        if (AS == 1) {
            jRadioButton1.setSelected(true);
        } else {
            jRadioButton2.setSelected(true);
        }

        try {
            Date dt = new SimpleDateFormat("yyyy-MM-dd").parse((String) dft.getValueAt(selectedIndex, 3));
            jDateChooser1.setDate(dt);
        } catch (ParseException ex) {
            Logger.getLogger(members.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jTable1MouseClicked

